export const useTranslation = (language) => {
    /**
     * t(key, varsOrDefault)
     * Usage:
     *   t('welcomeUser', { name: 'Sandeep' })
     *   // If not using variables: t('login')
     */
    const t = (key, varsOrDefault = {}) => {
        let str = translations[language]?.[key];
        let vars = {};
        if (typeof varsOrDefault === 'object' && varsOrDefault !== null && !Array.isArray(varsOrDefault)) {
            vars = varsOrDefault;
        } else {
            // If second arg is not an object, treat as default value
            str = str || varsOrDefault || key;
        }
        if (!str) str = key;
        // Replace {var} in string with values from vars
        if (typeof str === 'string' && Object.keys(vars).length > 0) {
            Object.entries(vars).forEach(([k, v]) => {
                str = str.replace(new RegExp(`{${k}}`, 'g'), v);
            });
        }
        return str;
    };

    /**
     * tLocation(locationName)
     * Helper function to translate location names (districts, subdistricts, cities)
     * Usage: tLocation('Bhopal') -> 'à¤­à¥‹à¤ªà¤¾à¤²' (in Hindi)
     */
    const tLocation = (locationName) => {
        if (!locationName || language === 'en') return locationName;

        // Convert to lowercase for lookup
        const lookupKey = locationName.toLowerCase();

        // Check if translation exists
        const translatedName = translations[language]?.[lookupKey];

        // Return translated name if found, otherwise return original
        return translatedName || locationName;
    };

    return { t, tLocation };
};